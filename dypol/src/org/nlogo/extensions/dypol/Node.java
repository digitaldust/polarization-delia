package org.nlogo.extensions.dypol;

/**
 *
 * @author Simone Gabbriellini
 */
public class Node {
    private int id;
    private String name;
    

    /**
     * @return the node
     */
    public int getId() {
        return id;
    }

    /**
     * @param node the node to set
     */
    public void setId(int id) {
        this.id = id;
    }
    /**
     * @return the node
     */
    public String getName() {
        return name;
    }

    /**
     * @param node the node to set
     */
    public void setName(String name) {
        this.name = name;
    }

    
}
